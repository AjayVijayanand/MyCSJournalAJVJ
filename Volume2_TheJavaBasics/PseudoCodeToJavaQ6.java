//FreeLancePseudoCodetoJava1 (Factorials)
package Volume2_TheJavaBasics;
public class PseudoCodeToJavaQ6 {
    public static void main(String args[]){
        int x = 0;
        int factorial = 1;
        for(int y = 1; y <= x; y++){
            factorial = factorial * y;
        }
        System.out.println(factorial);
    }
}
